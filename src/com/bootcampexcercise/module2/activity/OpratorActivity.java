package com.bootcampexcercise.module2.activity;

public class OpratorActivity {
    public static void main(String[] args) {
        int val1 = 12;
        int val2 = 5;

        System.out.println("val1 + val2 = " + (val1 + val2));

        System.out.println("val1 - val2 = " + (val1 - val2));

        System.out.println("val1 * val2 = " + (val1 * val2));

        System.out.println("val1 / val2 = " + (val1 / val2));

        System.out.println("val1 % val2 = " + (val1 % val2));
    }
}
